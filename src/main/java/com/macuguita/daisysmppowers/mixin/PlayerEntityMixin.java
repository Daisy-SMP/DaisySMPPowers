package com.macuguita.daisysmppowers.mixin;

import com.llamalad7.mixinextras.expression.Definition;
import com.llamalad7.mixinextras.expression.Expression;
import com.llamalad7.mixinextras.injector.ModifyExpressionValue;
import com.llamalad7.mixinextras.sugar.Local;
import com.macuguita.daisysmppowers.DaisySMPPowers;
import net.minecraft.entity.player.PlayerEntity;
import net.minecraft.util.math.Box;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;

@Mixin(PlayerEntity.class)
public class PlayerEntityMixin {

    @Definition(id = "box", local = @Local(type = Box.class))
    @Expression("box")
    @ModifyExpressionValue(
            method = "tickMovement",
            at = @At("MIXINEXTRAS:EXPRESSION")
    )
    public Box tickMovement$MobOrigins(Box original) {
        if (DaisySMPPowers.ITEM_COLLECTOR.isActive(((PlayerEntity)(Object)this))) {
            return original.expand(2);
        }
        return original;
    }
}
